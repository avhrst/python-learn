import text_processor

print(text_processor.remove_punctuation('Hello, World!'))
print(text_processor.convert_to_lowercase('Hello, World!'))
print(text_processor.tokenize_text('Hello, World!'))
print(text_processor.sentence_tokenize('Hello, World!'))
print(text_processor.word_count('Hello, World!'))
print(text_processor.character_count('Hello, World!'))
print(text_processor.unique_words('Hello, World!'))
print(text_processor.average_word_length('Hello, World!'))
print(text_processor.find_occurrences('Hello, World!', 'l'))


